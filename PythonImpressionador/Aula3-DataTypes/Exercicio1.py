# Exercício 1: Manipulando Números
# 1 - Crie uma variável para armazenar sua idade (como inteiro).
# 2 - Crie outra para armazenar sua altura (como decimal).
# 3 - Some as duas variáveis e veja o resultado.
# 4 - Multiplique o resultado por um número complexo (exemplo: 2 + 1j).

idade = 20
altura = 1.80

# Soma
soma = idade + altura
print(f"Soma: {soma}")

# Multiplicação com número complexo
numero_complexo = 20 + 30j
resultado = soma * numero_complexo
print(f"Resultado final: {resultado}")
