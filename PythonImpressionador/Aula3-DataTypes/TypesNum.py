#Exemplo de int
numero_inteiro = 10
print(f"Numero inteiro: {numero_inteiro} tipo: {type(numero_inteiro)}")

#Exemplo de float
numero_float = 10.5
print(f"Numero Decimal: {numero_float} tipo: {type(numero_float)}")

#Exemplo de numero complexo
numero_complexo = 2 + 3j
print(f"Numero Complexo: {numero_complexo} tipo: {type(numero_complexo)}")